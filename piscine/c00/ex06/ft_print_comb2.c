#include <unistd.h>

void	ft_print(int n)
{
	char	s[2];

	s[0] = n / 10 + '0';
	s[1] = n % 10 + '0';
	write(1, s, 2);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			ft_print(a);
			write(1, " ", 1);
			ft_print(b);
			if (a != 98)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
